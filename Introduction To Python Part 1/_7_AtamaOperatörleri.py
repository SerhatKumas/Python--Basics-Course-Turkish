#Atama Operatörleri

# = operatörü
number = 30

# += operatörü      number = number + x => number += x
number = number + 5  #number = 35
number += 5 #number = 40
print(number)

# -= operatörü      number = number - x => number -= x
number = number- 5 #number = 35
number -= 5 #number = 30
print(number)

# /= operatörü      number = number / x => number /= x
number /= 6 #number = 5
print(number)

# *= operatörü      number = number * x => number *= x
number *= 5 #number = 25
print(number)

# **= operatörü     number = number ** x => number **= x
number **= 2 #number = 625
print(number)

# %= operatörü      number = number % x => number %= x
number %= 600 # number = 25
print(number)

# //= operatörü     number = number // x => number //= x
number //= 4  #number = 6
print(number)











