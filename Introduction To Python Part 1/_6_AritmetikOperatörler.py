#******************************************   Aritmetik OPERATÖRLER   ***********************************************

#Aritmetik Operatörleri : Matematiksel işlemleri gerçekleştirmek için kullanılırlar

# Toplama : +
print(8+7)

# Çıkartma : -
print(2-5)

# Çarpma : *
print(9*6)

# Bölme : /
print(10/4)

# Tam Sayı bölen operatörü // (Bölme yaparken ondalıklı değilde tam sayı sonuçlar verir)
print(10//4)

# Mod alma veya kalan bulma operatörü %
print(51%4) #2 çıkar 1 kalanı

# Üs alma operatörü **  (taban ** üs)
print(2*2)
print(4**3)
print(2**20)

