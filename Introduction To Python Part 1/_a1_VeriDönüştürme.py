#************************************* Veri Dönüştürme ********************************************************

#int methodu() bir tam sayı oluşturur. parantez içinde belirtilen değer float ise ondalık kısım silinir, str ise ifade ettiği sayı tespit edilir.
variable = int(1)   # x 1 olacaktır
variable1 = int(2.8) # y 2 olacaktır
variable2 = int("3") # z 3 olacaktır

#float methodu() kesirsel/ondalıklı ifade oluşturur. tam sayı ya da metin değerlerle kullanılabilir.
variable3 = float(1)     # x 1.0 olacaktır
variable4 = float(2.8)   # y 2.8 olacaktır
variable5 = float("3")   # z 3.0 olacaktır
variable6 = float("4.2") # w 4.2 olacaktır

#str methodu () birçok veri türünü metin ifadeye çevirir, bunlara sayı ve kesir ifadeler de dahildir.
variable7 = str("s1") # x 's1' olacaktır
variable8 = str(2)    # y '2' olacaktır
variable9 = str(3.0)  # z '3.0' olacaktır