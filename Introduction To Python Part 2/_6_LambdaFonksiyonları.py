#*************************************** Lambda Fonksiyonları ****************************************************

#Python'da lambda, tek satırlık fonksiyonlardır. Bir ya da daha fazla parametre kabul ederler, ancak tek bir işlem yapabilirler
x = lambda a : a + 10
print(x(5))

#Bir de iki parametreli tanımlayalım
topla = lambda a, b : a + b
print(topla(5, 6))
